    const name = 'bp-icon';

    const iconClass = computed(() => [name, `${name}-electronic-locks-open`, { [`${name}-spin`]: props.spin }]);

    const innerStyle = computed(() => {
      const styles: CSSProperties = {};
        props.size && (styles.width = props.size);
